package academy.devdojo.maratonajava.introducao;

public class Aula04OperadoresJava {
    public static void main(String[] args) {
        int contador = 0;
        contador += 1;
        contador++;
        contador--;
        ++contador;
        --contador;
        int contador2 = 0;
        System.out.println(++contador2);
        System.out.println(contador2++);
    }
}

